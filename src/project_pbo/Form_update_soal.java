    private void tb_soalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_soalMouseClicked
        // TODO add your handling code here:
        int row = tb_soal.rowAtPoint(evt.getPoint());
        id_jawaban = Integer.parseInt(tb_soal.getValueAt(row, 0).toString());
        String kategori = tb_soal.getValueAt(row, 1).toString();
        try {
            hasil_soal = soal.searchByIdJawaban(id_jawaban);
            hasil_soal.next();

            jawaban.setPil_A(hasil_soal.getString("pilihan_a"));
            jawaban.setPil_B(hasil_soal.getString("pilihan_b"));
            jawaban.setPil_C(hasil_soal.getString("pilihan_c"));
            jawaban.setPil_D(hasil_soal.getString("pilihan_d"));
            jawaban.setKunci(hasil_soal.getString("kunci"));

            txt_soal.setText(hasil_soal.getString("isi_soal"));
            txt_pil_a.setText(jawaban.getPil_A());
            txt_pil_b.setText(jawaban.getPil_B());
            txt_pil_c.setText(jawaban.getPil_C());
            txt_pil_d.setText(jawaban.getPil_D());
            cb_kategori.setSelectedItem(kategori);

            if (jawaban.getKunci().equals(txt_pil_a.getText())) {
                cb_kunci.setSelectedItem("Pilihan A");
            } else if (jawaban.getKunci().equals(txt_pil_b.getText())) {
                cb_kunci.setSelectedItem("Pilihan B");
            } else if (jawaban.getKunci().equals(txt_pil_c.getText())) {
                cb_kunci.setSelectedItem("Pilihan C");
            } else if (jawaban.getKunci().equals(txt_pil_d.getText())) {
                cb_kunci.setSelectedItem("Pilihan D");
            }
        } catch (Exception e) {

        }
    }//GEN-LAST:event_tb_soalMouseClicked

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        String kunci = cb_kunci.getSelectedItem().toString();

        soal.setKategori(cb_kategori.getSelectedItem().toString());
        soal.setIsi_soal(txt_soal.getText());
        jawaban.setPil_A(txt_pil_a.getText());
        jawaban.setPil_B(txt_pil_b.getText());
        jawaban.setPil_C(txt_pil_c.getText());
        jawaban.setPil_D(txt_pil_d.getText());

        switch (kunci) {
            case "Pilihan A":
                jawaban.setKunci(jawaban.getPil_A());
                break;
            case "Pilihan B":
                jawaban.setKunci(jawaban.getPil_B());
                break;
            case "Pilihan C":
                jawaban.setKunci(jawaban.getPil_C());
                break;
            case "Pilihan D":
                jawaban.setKunci(jawaban.getPil_D());
                break;
        }
        
        jawaban.update(jawaban.getPil_A(), jawaban.getPil_B(), jawaban.getPil_C(), jawaban.getPil_D(), jawaban.getKunci(), id_jawaban);
        soal.update(soal.getKategori(), soal.getIsi_soal(), id_jawaban);
        
        clear_form();
        tampil_soal();

    }//GEN-LAST:event_btn_updateActionPerformed

