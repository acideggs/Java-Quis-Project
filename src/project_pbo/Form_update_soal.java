    void tampil_soal() {
        Object[] header = {"no", "Kategori", "Soal"};
        model_tabel = new DefaultTableModel(null, header);
        tb_soal.setModel(model_tabel);
        tb_soal.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_OFF);
        tb_soal.getColumnModel().getColumn(0).setPreferredWidth(65);
        tb_soal.getColumnModel().getColumn(1).setPreferredWidth(100);
        tb_soal.getColumnModel().getColumn(2).setPreferredWidth(450);

        try {
            hasil = soal.getData();
            while (hasil.next()) {
                soal.setKategori(hasil.getString("kategori"));
                soal.setIsi_soal(hasil.getString("isi_soal"));
                String[] isi = {hasil.getString("id_jawaban"), soal.getKategori(), soal.getIsi_soal()};
                model_tabel.addRow(isi);
            }
        } catch (Exception e) {

        }
    }

